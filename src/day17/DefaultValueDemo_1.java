package day17;

public class DefaultValueDemo_1 {
	
	//import interview question ???
	//default value of variable which is inside class
	//a local variable is must initialized
	//a local variable do not have default value but a variable declared in a class will take default value
	
	
	byte bb ;
	short ss;
	int a;
	long l;
	String s;
	boolean b; 
	double d;
	float f;
	char c; 

	public static void main(String[] args) {
		
		DefaultValueDemo_1 ob = new DefaultValueDemo_1(); 
		
		System.out.println(ob.bb);
		System.out.println(ob.ss);
		System.out.println(ob.a);
		System.out.println(ob.l);
		System.out.println(ob.s);
		System.out.println(ob.b);
		System.out.println(ob.d);
		System.out.println(ob.f);
		System.out.println(ob.c);
		

	}

}
