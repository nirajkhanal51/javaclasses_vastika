package day19;

public class ArrayDemo8 {

			
		public static void main(String[] args) {
			
			int [] [] arr = new int [] [] {{23,34,34},{34,45,67},{99,88,77}};
			
			for (int i = 0; i < arr.length; i++) { 
				
				for (int j = 0; j < arr.length; j++) { 
					System.out.print(arr[i][j] + " ");
				}
				
				System.out.println(); 																								
			
		}
		}
	}