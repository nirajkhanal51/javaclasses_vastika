package day6;

public class OperatorDemo2 {

	public static void main(String[] args) {
		// 3. increment/decrement operator
		// ++    --
		//post increment   a++
		//pre  increment   ++a
		//post decrement   a--
		//pre  decrement   --a 
		
		int i = 4;
		System.out.println(i);
		
		i = i + 2;
		System.out.println(i);
		
		//using shorthand operator for above expression
		i += 2;
		System.out.println(i);

		// for increment of 1
		i += 1;
		System.out.println(i);
		
		// the above expression can be written as 
		i++;
		System.out.println(i);
		
		
		
		
	}

}














